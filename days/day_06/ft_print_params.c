void	ft_putchar(char c);

void	ft_putstr(char *str)
{
	while (*str)
		ft_putchar(*str++);
}

int		main(int argc, char **argv)
{
	while (argc >= 2)
	{
		argv++;
		ft_putstr(*argv);
		ft_putchar('\n');
		argc--;
	}
	return (0);
}
