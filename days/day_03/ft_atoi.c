int		ft_atoi(char *str)
{
	int		nbr;
	int		i;

	i = 0;
	nbr = 0;
	while (str[i])
	{
		if (str[i] >= '0' && str[i] <= '9')
			nbr = str[i] - '0' + nbr * 10;
		else if (i != 0)
			return (0);
		i++;
	}
	if (str[0] == '-')
		nbr *= -1;
	return (nbr);
}
